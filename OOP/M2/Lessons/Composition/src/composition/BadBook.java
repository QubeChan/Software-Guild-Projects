public class BadBook {
    private String title,
            isbn,
            authorName,
            authorStreet,
            authorCity,
            authorState,
            authorZip,
            publisherName,
            publisherStreet,
            publisherCity,
            publisherState,
            publisherZip,
            publisherPhone;

    public BadBook() {
    }
    
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public String getAuthorStreet() {
        return authorStreet;
    }

    public void setAuthorStreet(String authorStreet) {
        this.authorStreet = authorStreet;
    }

    public String getAuthorCity() {
        return authorCity;
    }

    public void setAuthorCity(String authorCity) {
        this.authorCity = authorCity;
    }

    public String getAuthorState() {
        return authorState;
    }

    public void setAuthorState(String authorState) {
        this.authorState = authorState;
    }

    public String getAuthorZip() {
        return authorZip;
    }

    public void setAuthorZip(String authorZip) {
        this.authorZip = authorZip;
    }

    public String getPublisherName() {
        return publisherName;
    }

    public void setPublisherName(String publisherName) {
        this.publisherName = publisherName;
    }

    public String getPublisherStreet() {
        return publisherStreet;
    }

    public void setPublisherStreet(String publisherStreet) {
        this.publisherStreet = publisherStreet;
    }

    public String getPublisherCity() {
        return publisherCity;
    }

    public void setPublisherCity(String publisherCity) {
        this.publisherCity = publisherCity;
    }

    public String getPublisherState() {
        return publisherState;
    }

    public void setPublisherState(String publisherState) {
        this.publisherState = publisherState;
    }

    public String getPublisherZip() {
        return publisherZip;
    }

    public void setPublisherZip(String publisherZip) {
        this.publisherZip = publisherZip;
    }

    public String getPublisherPhone() {
        return publisherPhone;
    }

    public void setPublisherPhone(String publisherPhone) {
        this.publisherPhone = publisherPhone;
    }
    
    
}
